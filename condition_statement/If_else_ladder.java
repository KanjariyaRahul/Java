class If_else_ladder {


    public static void main(String[] args) 
    { 
        
       char c ='A';
       
        if(c =='A')
        {
            System.out.println("You Select A");
        }
        else if (c == 'B') 
        {
            System.out.println("You Select B");
            
        } else 
        {
            System.out.println("You Select Other Character");
        }
        
    }
    
}
